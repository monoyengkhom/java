import java.lang.*;
public class literal {
    public static void main(String[] args) {
        byte b1 = 10;
        byte b2 = 0b1010;
//        byte b3 = 0b12;
        byte b4 = 0XA;
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b4);

        System.out.println("\n");

        long l = 9999999999L;
        System.out.println(l);
        System.out.println("\n");

        float f = 12.56f;
        double d = 12.56d;
        System.out.println(f);
        System.out.println(d);
        System.out.println("\n");
    }
}
